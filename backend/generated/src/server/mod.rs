/// Setup API Server.
pub fn new<I, A>(api_impl: I) -> Router
where
    I: AsRef<A> + Clone + Send + Sync + 'static,
    A: Api + 'static,
{
    // build our application with a route
    Router::new()
        .route("/auth/login",
            post(auth_login_post::<I, A>)
        )
        .route("/auth/logout",
            post(auth_logout_post::<I, A>)
        )
        .route("/auth/me",
            get(auth_me_get::<I, A>)
        )
        .route("/task",
            get(task_get::<I, A>).post(task_post::<I, A>)
        )
        .route("/task/:task_id",
            get(task_task_id_get::<I, A>)
        )
        .with_state(api_impl)
}

